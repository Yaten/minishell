#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	if (s1 == NULL)
		return (1);
	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return ((int)(unsigned char)(*s1) - (int)(unsigned char)(*s2));
}
